async function destinationPlaylist() {
    if (playlistCompleted) {
        console.log("Playlist already completed. Exiting early.");
        return;
    }

    startTimer();
    console.log('Timer starts!');

    const selectedCountryId = document.getElementById('countryDropdown').value;
    const selectedDecadeId = document.getElementById('decadeDropdown').value;
    const randomCountry = countries.find(country => country.name === selectedCountryId);
    const randomDecadeObj = decades.find(decade => decade.name === selectedDecadeId);
    const decadeRange = randomDecadeObj.range.split('-').map(Number);

    let chosenGenre = randomCountry.genres[Math.floor(Math.random() * randomCountry.genres.length)];
    console.log('Chosen Country:', randomCountry.name);
    console.log('Chosen Decade:', randomDecadeObj.name);
    console.log('Chosen Genre:', chosenGenre);

    let allArtists = [];
    for (let year = decadeRange[0]; year <= decadeRange[1]; year++) {
        if (playlistCompleted) return;
        const artists = await getAllArtistsForGenre(chosenGenre, year, 50, 0);
        if (artists.length > 0) allArtists.push(...artists);
    }

    if (allArtists.length === 0) {
        console.log(`No artists found for ${chosenGenre} in ${randomDecadeObj.name}, trying another genre...`);
        return await destinationPlaylist(); // Retry with a different genre
    }

    let triedArtists = new Set();
    while (allArtists.length > 0 && finalPlaylist.length < 20) {
        const artistIndex = Math.floor(Math.random() * allArtists.length);
        const chosenArtist = allArtists.splice(artistIndex, 1)[0];

        if (triedArtists.has(chosenArtist.id)) continue;
        triedArtists.add(chosenArtist.id);

        const success = await fetchAlbumsForArtistAndDecade(chosenArtist.name, decadeRange[0], decadeRange[1], randomCountry.name, randomDecadeObj.name);
        if (success) {
            if (finalPlaylist.length >= 20) {
                playlistCompleted = true;
                console.log('Final playlist completed:', finalPlaylist);
                return;
            }
        } else {
            console.log('No albums or tracks found, retrying with another artist...');
        }
    }

    if (!playlistCompleted && allArtists.length === 0) {
        console.log('No more artists available, trying again with another genre...');
        return await destinationPlaylist(); // Retry with a different genre if all artists are exhausted
    }
}


async function getAllArtistsForGenre(genre, year, limit, offset) {
    try {
        const tokenResponse = await fetch('/get-token');
        const tokenData = await tokenResponse.json();

        if (!tokenData.accessToken) {
            console.error('No access token available');
            return [];
        }

        const url = `/api/artists?genre=${encodeURIComponent(genre)}&year=${year}&limit=${limit}&offset=${offset}`;
        const response = await fetch(url, {
            method: 'GET',
            headers: {
                'Authorization': `Bearer ${tokenData.accessToken}`,
                'Content-Type': 'application/json'
            }
        });

        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const data = await response.json();
        return data.artists.items; // Assuming the response includes artists in this structure
    } catch (error) {
        console.error(`Error fetching artists for genre ${genre} in year ${year}:`, error);
        return [];
    }
}

